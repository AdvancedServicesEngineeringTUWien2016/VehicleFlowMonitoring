package vfma;

public class SensorMap {
	
	private String lowerZoneId;
	
	private String upperZoneId;
	
	private String sensorId;
	
	public String getLowerZoneId() {
		return lowerZoneId;
	}

	public void setLowerZoneId(String lowerZoneId) {
		this.lowerZoneId = lowerZoneId;
	}

	public String getUpperZoneId() {
		return upperZoneId;
	}

	public void setUpperZoneId(String upperZoneId) {
		this.upperZoneId = upperZoneId;
	}

	public String getSensorId() {
		return sensorId;
	}

	public void setSensorId(String sensorId) {
		this.sensorId = sensorId;
	}



}
